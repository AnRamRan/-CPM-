function animate(tick, partial, frames, length) {
    if(length ==0) {
        return [frames[1], frames[2], frames[3]];
    }
    var last = Math.floor(frames.length/4);
    var time = (tick/20) % length + partial/20.;
    for(var i = 1; i < last; i++) {
        if (time >= frames[4*(i-1)] & time < frames[4*i]) {
            var t = (time - frames[4*(i-1)]) / (frames[4*i] - frames[4*(i-1)]);
            return [t*(frames[4*i+1] - frames[4*(i-1)+1]) + frames[4*(i-1)+1], t*(frames[4*i+2] - frames[4*(i-1)+2]) + frames[4*(i-1)+2], t*(frames[4*i+3] - frames[4*(i-1)+3]) + frames[4*(i-1)+3]];
        }
    }
    return [frames[4*last-3],frames[4*last-2],frames[4*last-1]];
} 

